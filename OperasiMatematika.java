public class OperasiMatematika {
    public static void main(String[] args) {

        //Math Operator
        int a = 10;
        int b = 30;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        //Augmented Assigments (+=, -=, *=, /=, %=)
        int c = 100;

        c += 10; //c = c + 10
        System.out.println(c);

        c -= 10; //c = c - 10
        System.out.println(c);

        c *= 10; //c = c * 10
        System.out.println(c);

        //Unary Operator (++, --, -, +, !)
        int d = 100;

        d++; // d - 1
        System.out.println(d);

        d--; // d - 1
        System.out.println(d);

    }
}
