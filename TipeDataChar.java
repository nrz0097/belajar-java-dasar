public class TipeDataChar {
    public static void main(String[] args) {

        //Value tipe data char menggunakan petik satu ''
        char z = 'Z';
        char e = 'E';
        char n = 'N';

        System.out.println(z);
        System.out.println(e);
        System.out.println(n);

        //Gunakan "System.out.print()" untuk print hasil secara horizontal
        System.out.print(z);
        System.out.print(e);
        System.out.print(n);

    }
}
